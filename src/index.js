function Footer() {
  const hour = new Date().getHours();
  const openHour = 12;
  const closeHour = 22;
  const isOpen = hour >= openHour && hour <= closeHour;

  return (
    <footer className="footer">
      {isOpen ? (
        <OrderHours closing={closeHour} />
      ) : (
        <p>Sorry we are closed till {openHour}</p>
      )}
    </footer>
  );
}

function OrderHours(props) {
  return (
    <div className="order">
      <p>We are open untill {props.closing}:00. Visit us or order online.</p>
      <button className="btn">Order</button>
    </div>
  );
}

